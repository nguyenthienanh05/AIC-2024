const handleSearch = async (e) => {
  e.preventDefault();
  const regex = /(\w+)\s+(\d+)/;
  const match = searchInput.match(regex);

  if (match) {
    const [, videoName, tempFrameIndex] = match;
    const fps = fps_json[videoName];

    if (fps) {
      const currentVideoTime = parseInt(tempFrameIndex) / fps;
      const videoSrc = `https://storage.googleapis.com/aic_final_2024/${videoName}/${videoName}.mp4`;

      setVideoDetails({
        videoName,
        frameNumber: tempFrameIndex,
        videoSrc,
        frameIndex: parseInt(tempFrameIndex),
        timestamp: currentVideoTime,
        fps,
      });

      setIsModalOpen(true);
    } else {
      alert('FPS not found for the given video name.');
    }
  } else {
    alert('Invalid input format. Please use the format: L02_V001 3025');
  }
};

